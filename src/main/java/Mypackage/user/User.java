package Mypackage.user;


public class User {

    private String name;
    private String id;
    private int age;
    private Kinds kinds;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Kinds getKinds() {
        return kinds;
    }

    public void setKinds(Kinds kinds) {
        this.kinds = kinds;
    }


}
